package com.musala.javacourse181112.tasks.calculatorexercise;

public class Person {
    private int age;
    private int name;
    private int ageToAdd;

    public int getAgeToAdd() {
        return ageToAdd;
    }

    public void setAgeToAdd(int ageToAdd) {
        this.ageToAdd = ageToAdd;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }
}
